# Step 1: Create the initial inventory list
inventory = ["Apples", "Bananas", "Carrots", "Dates"]

# Step 2: Print the current inventory
print("Current Inventory:", inventory)

# Step 3: Add "Eggs" to the inventory using append()
inventory.append("Eggs")

# Step 4: Remove "Bananas" from the inventory using remove()
inventory.remove("Bananas")

# Step 5: Sort the inventory alphabetically using sort()
inventory.sort()

# Step 6: Print the final updated inventory
print("Updated Inventory:", inventory)
